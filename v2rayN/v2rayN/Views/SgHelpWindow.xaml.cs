    public SgHelpWindow(string initialSection = "start")
    {
        InitializeComponent();
        SgWindowSizing.AttachLarge(this);
        SourceInitialized += (_, _) => WindowsUtils.SetSgBorderlessFrame(this);
        _initialSection = Sections.ContainsKey(initialSection) ? initialSection : "start";
        Loaded += (_, _) => SelectSection(_initialSection);
    }

    private void SelectSection(string tag)
    {
        foreach (ListBoxItem item in lstSections.Items)
        {
            if (string.Equals(item.Tag?.ToString(), tag, StringComparison.OrdinalIgnoreCase))
            {
                lstSections.SelectedItem = item;
                item.BringIntoView();
                return;
            }
        }
    }

    private void Sections_SelectionChanged(object sender, SelectionChangedEventArgs e)
    {
        if (lstSections.SelectedItem is not ListBoxItem item || item.Tag is null)
        {
            return;
        }

        var section = Sections.GetValueOrDefault(item.Tag.ToString()!, Sections["start"]);
        txtTitle.Text = section.Title;
        txtBody.Text = section.Body;
    }

    private void Header_MouseLeftButtonDown(object sender, MouseButtonEventArgs e)
    {
        if (e.LeftButton == MouseButtonState.Pressed)
        {
            DragMove();
        }
    }

    private void Close_Click(object sender, RoutedEventArgs e)
    {
        Close();
    }
