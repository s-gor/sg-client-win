SG Client читает путь журнала из фактически запущенного config.json, поэтому функция работает одинаково в TUN, System Proxy и Local Proxy. Если настройка уже включена, но Xray запущен со старой конфигурацией, появится кнопка «Переподключить Xray».
Записываются только домен/IP, порт, TCP/UDP и выбранный Output; содержимое трафика и секреты не записываются. В настройках переключатель называется «Журнал соединений Xray».

ЧТО СМОТРЕТЬ
• точное имя запущенного ядра;
• путь к config.json;
• первую ошибку перед повторяющимися сообщениями;
• занятый порт;
• отказ доступа;
• ошибку синтаксиса или неизвестный параметр;
• автоматический откат после неудачной замены.

